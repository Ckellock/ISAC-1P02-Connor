function  startTime() {
  var today = new Date();
  var h = today.getHours();
  var m = today.getMinutes();
  var s = today.getSeconds();
  m = checkTime(m);
  s = checkTime(s);
  document.getElementById('estHeader').innerHTML =
  h + ":" + m + ":" + s;
  var t = setTimeout(startTime, 500);
}
